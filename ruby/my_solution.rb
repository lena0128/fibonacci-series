# In mathematics, the Fibonacci numbers, commonly denoted Fn, form a sequence, called the Fibonacci sequence, 
# such that each number is the sum of the two preceding ones, starting from 0 and 1. 
# Fn = 0, F1 = 1
# n >= 2, Fn = F(n-1) + F(n -2)

